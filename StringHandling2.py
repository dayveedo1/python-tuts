# Capitalize ->  replaces the first letter in a string to Upper Case
name = "emeka"
print(name.capitalize())

# center ->  to align to center a string leaving both side with whitespaces
newName = "David"
newName2 = "Emeka"
print(newName.center(15, " "))
print(newName2.center(15, "*"))

# find -> to find a string within a string
print(name.find("ka"))

